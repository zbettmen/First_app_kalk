package se.ECUTB.RijadHamula;

public class Kalk {


    public double add(double kalk1, double kalk2){

        double kalk3;
        kalk3 = kalk1 + kalk2;
        return  kalk3;
    }
    public double sub(double kalk1, double kalk2){

        double kalk3;
        kalk3 = kalk1 - kalk2;
        return  kalk3;
    }
    public double multi(double kalk1, double kalk2){

        double kalk3;
        kalk3 = kalk1*kalk2;
        return  kalk3;
    }
    public double div(double kalk1, double kalk2){

        double kalk3;
        kalk3 = kalk1/kalk2;
        return  kalk3;
    }


}


